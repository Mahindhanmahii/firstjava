class Signal
{
	public static void main(String[]args)
	{
		 char signal='Y';
		 
		 
		
		if(signal=='R')
		{
			System.out.println("STOP");
			
			
		}
		else if (signal=='Y')
		{
			
			System.out.println("READY");
			
		}
		else if (signal=='G')
		{
			
			System.out.println("GO");
			
		}
		else 
		{
			System.out.println("NOT ALLOWED");
			
			
		}
		
		
		
		
		
	}
}
	
	